public class ManagerVO {
	
	public String ms_num;
	public String ms_id;
	public String ms_name;
	public String ms_sex;
	public String ms_birth;
	public String ms_type;
	
	
	
	public String getMs_type() {
		return ms_type;
	}
	public void setMs_type(String ms_type) {
		this.ms_type = ms_type;
	}
	public String getMs_num() {
		return ms_num;
	}
	public void setMs_num(String ms_num) {
		this.ms_num = ms_num;
	}
	public String getMs_id() {
		return ms_id;
	}
	public void setMs_id(String ms_id) {
		this.ms_id = ms_id;
	}
	public String getMs_name() {
		return ms_name;
	}
	public void setMs_name(String ms_name) {
		this.ms_name = ms_name;
	}
	public String getMs_sex() {
		return ms_sex;
	}
	public void setMs_sex(String ms_sex) {
		this.ms_sex = ms_sex;
	}
	public String getMs_birth() {
		return ms_birth;
	}
	public void setMs_birth(String ms_birth) {
		this.ms_birth = ms_birth;
	}
	
}

